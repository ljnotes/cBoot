int main()
{
    // Variable holds a reference to values.
    // The value types in C are:
    // int - Integer
    // float - Floating-point
    // char - Character value
    // double
    // long
    // short
    // Note: C doesn't have type to hold string, instead we use character array

    int value = 20;
    value = value - 10;

    printf("I am the value: %d\n", value);

    float pi = 22.0 / 7.0;
    printf("The value of PI is %f\n", pi);

}
